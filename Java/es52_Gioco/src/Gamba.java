public class Gamba {
    private String colore;
    private int lunghezza;

    public Gamba(String colore, int lunghezza) {
        this.colore = colore;
        this.lunghezza = lunghezza;
    }

    public String getColore() {
        return colore;
    }

    public int getLunghezza() {
        return lunghezza;
    }
}
