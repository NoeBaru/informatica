public class Arto {
    private String colore;
    private int percForza;

    public Arto(String colore, int percForza) {
        this.colore = colore;
        this.percForza = percForza;
    }

    public String getColore() {
        return colore;
    }

    public int getPercForza() {
        return percForza;
    }
}
